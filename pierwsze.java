class Zajecia {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        int suma = a + b;
        int roznica = a - b;
        int iloczyn = a * b;
        double iloraz = (double) a / b;

        System.out.println("Suma: " + suma);
        System.out.println("Roznica: " + roznica);
        System.out.println("Iloczyn: " + iloczyn);
        System.out.println("Iloraz: " + iloraz);
    }
}
